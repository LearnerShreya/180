#include<iostream>
using namespace std;

int main()
{
    // Bitwise Operator

    // & Operator
    int ans = 2&3;
    cout<<"My answer is: "<<ans<<endl;
    // | Operator
    int ant = 2|3;
    cout<<"My answer is: "<<ant<<endl;
    // ^ Operator
    int anu = 2^3;
    cout<<"My answer is: "<<anu<<endl;
    // >> Operator
    int anv = 10>>3;
    cout<<"My answer is: "<<anv<<endl;
    // << Operator
    int anw = 2<<3;
    cout<<"My answer is: "<<anw<<endl;
    // ~ Operator
    int anx = ~5;
    cout<<"My answer is: "<<anx<<endl;
    // ~ Operator
    int any = ~-15;
    cout<<"My answer is: "<<any<<endl;
    
}