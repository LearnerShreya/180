#include<iostream>
using namespace std;

int main()
{
    // Assignment Operator

    int a = 10;
    a+=15; // a = a + 15;
    cout<<"Answer is: ";
    cout<<a;
    cout<<endl;

    int b = 10;
    b-=15; // b = b - 15;
    cout<<"Answer is: ";
    cout<<b;
    cout<<endl;

    int c = 10;
    c*=15; // c = c * 15;
    cout<<"Answer is: ";
    cout<<c;
    cout<<endl;

    int d = 10;
    d/=15; // d = d / 15;
    cout<<"Answer is: ";
    cout<<d;
    cout<<endl;

    int e = 10;
    e%=15; // e = e % 15;
    cout<<"Answer is: ";
    cout<<e;
    cout<<endl;
    
}